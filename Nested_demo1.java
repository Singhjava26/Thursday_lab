
public class Nested_demo1 
{

	public static void main(String[] args)
	{
		int rows = 5;
	    for (int i = 1; i <= rows; ++i)// outer loop
	    {
	      for (int j = 1; j <= i; ++j) // inner loop 
	      {
	        System.out.print(j + "0");
	      }
	      System.out.println("");

	    }

     }
}
